function abrirModal() {
    var modal = document.querySelector('section.container_modal')

    modal.style.display = 'block'
}


function fecharModal() {
    var modal = document.querySelector('section.container_modal')

    modal.style.display = 'none'
}



function mostrar() {
    var mensagem = document.querySelector('article.dialogo')

    mensagem.style.display = 'block'
}


function ocultar() {
    var mensagem = document.querySelector('article.dialogo')

    mensagem.style.display = 'none'
}
